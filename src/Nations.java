
public class Nations {
	String name;
	int total_exports;
	int total_export_sum;
	int total_imports;
	int total_import_sum;
	public Nations(){
		total_exports=0;
		total_export_sum=0;
		total_imports=0;
		total_import_sum=0;
		
	}
	public Nations(String nation){
		name = nation;
		total_exports=0;
		total_export_sum=0;
		total_imports=0;
		total_import_sum=0;
	}

}
